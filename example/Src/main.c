/**
* @brief Interrupt callback for GPIOs
*/
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
    if ( GPIO_Pin == GPIO_PIN_6) {
    	if(setup_done){
    		uint8_t val[5] = {0,0,0,0,0};
    		uint8_t cmd[5] = {0,0,0,0,0};
    		cmd[0] = MCP3561_SREAD_DATA_COMMAND;
    		HAL_GPIO_WritePin(SPI1_CS_GPIO_Port, SPI1_CS_Pin, 0);
    		// non-blocking "HAL_SPI_TransmitReceive_IT" does not work
    		// because we need to create CS signal manually
    		// the timout therefore must not be greater than 1/fs
    		// e.g. at 300 Hz --> 3ms
    		HAL_SPI_TransmitReceive(&hspi1, &cmd[0], &val[0], 5, 1);
    		HAL_GPIO_WritePin(SPI1_CS_GPIO_Port, SPI1_CS_Pin, 1);
    		adc_val = (val[1] << 16) | (val[2] << 8) | val[3];
    	}
    }
}

/*@brief this fuction does One-Shot for MCP3564 */
void ADC_One_Shot(SPI_HandleTypeDef *hspi)
{
 int status;
 int notReady;

 // Pull chip select low
 HAL_GPIO_WritePin(SPI1_CS_GPIO_Port, SPI1_CS_Pin, 0);

 // Write command byte and get status in return
 uint8_t cmd;
 cmd = readConversionData;
 HAL_SPI_TransmitReceive(hspi, &cmd,0, 1, 10);

 // Check the status byte for the POR bit. Low = POR has occurred
 if ((status & 0x01) == 0x00)
 {
	 //Use your own debug mode
	 //Lcd_cursor(&lcd,0,9);
	 //Lcd_string (&lcd,"ADC POR");
     MCP3561_Init_ch0(&hspi1);
  return;
 }

 // Check the data ready bit
 if ((status & 0x04) == 0x04)
 {
  notReady++;
  if (notReady > 20)
  {
	  //Use your own debug mode
	  //Lcd_cursor(&lcd,0,9);
	  //Lcd_string (&lcd,"ADC not ready");
  }
  else
  {
	  HAL_GPIO_WritePin(SPI1_CS_GPIO_Port, SPI1_CS_Pin, 1);
   return;
  }
 }
 notReady = 0;


 // Set chip select high again
 HAL_GPIO_WritePin(SPI1_CS_GPIO_Port, SPI1_CS_Pin, 1);

 // Start the next encode
 cmd = startConversionFastCmd;
 HAL_GPIO_WritePin(SPI1_CS_GPIO_Port, SPI1_CS_Pin, 0);
 HAL_SPI_TransmitReceive(hspi, &cmd, 0, 1, 10);
 HAL_GPIO_WritePin(SPI1_CS_GPIO_Port, SPI1_CS_Pin, 1);

}

